package com.yourcat.yourcat.test;

public class Person {

	private int sex;
	
	private int age;

	public Person(int sex, int age) {
		super();
		this.sex = sex;
		this.age = age;
	}

	public int getSex() {
		return sex;
	}

	public void setSex(int sex) {
		this.sex = sex;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}
	
}
